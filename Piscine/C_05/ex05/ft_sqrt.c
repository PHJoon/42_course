int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	else
	{
		while (i <= (double)nb / i)
		{
			if ((double)nb / i == i)
				return (i);
			i++;
		}
		return (0);
	}
}
